package com.is.inventory.model;

public class ProductOption {

	private int ID;
	private String Name;
	/**
	 * @return the ID
	 */
	public int getID() {
		return ID;
	}
	/**
	 * @param ID the ID to set
	 */
	public void setID(int ID) {
		this.ID = ID;
	}
	/**
	 * @return the name
	 */
	public String getName() {
		return Name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		Name = name;
	}
	
	
}
