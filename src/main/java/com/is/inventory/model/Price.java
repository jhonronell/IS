package com.is.inventory.model;

public class Price {

	private float price;
	private float msrp;
	/**
	 * @return the price
	 */
	public float getPrice() {
		return price;
	}
	/**
	 * @param price the price to set
	 */
	public void setPrice(float price) {
		this.price = price;
	}
	/**
	 * @return the msrp
	 */
	public float getMsrp() {
		return msrp;
	}
	/**
	 * @param msrp the msrp to set
	 */
	public void setMsrp(float msrp) {
		this.msrp = msrp;
	}
	
}
