package com.is.inventory.model;

public class ContactInformation {

	private int ID;
	private String contactInfoType;
	private String contactInfoVale;
	/**
	 * @return the iD
	 */
	public int getID() {
		return ID;
	}
	/**
	 * @param iD the iD to set
	 */
	public void setID(int iD) {
		ID = iD;
	}
	/**
	 * @return the contactInfoType
	 */
	public String getContactInfoType() {
		return contactInfoType;
	}
	/**
	 * @param contactInfoType the contactInfoType to set
	 */
	public void setContactInfoType(String contactInfoType) {
		this.contactInfoType = contactInfoType;
	}
	/**
	 * @return the contactInfoVale
	 */
	public String getContactInfoVale() {
		return contactInfoVale;
	}
	/**
	 * @param contactInfoVale the contactInfoVale to set
	 */
	public void setContactInfoValue(String contactInfoVale) {
		this.contactInfoVale = contactInfoVale;
	}
	
	
	
	
}
